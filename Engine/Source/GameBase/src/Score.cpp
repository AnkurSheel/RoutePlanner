#include "stdafx.h"
#include "Score.h"

using namespace GameBase;
using namespace Base;

//  *******************************************************************************************************************
cScore::cScore()
: m_Score(0)
{
}

//  *******************************************************************************************************************
cScore::cScore(const cString & PlayerName, const int Score)
: m_PlayerName(PlayerName)
, m_Score(Score)
{
}

//  *******************************************************************************************************************
cScore::~cScore()
{
}

//  *******************************************************************************************************************
void cScore::UpdateScore(const int DeltaValue)
{
	m_Score += DeltaValue;
}

//  *******************************************************************************************************************
void cScore::SetScore(const int Score)
{
	m_Score = Score;
}

//  *******************************************************************************************************************
int cScore::GetScore() const
{
	return m_Score;
}

//  *******************************************************************************************************************
Base::cString cScore::GetPlayerName() const
{
	return m_PlayerName;
}

//  *******************************************************************************************************************
void cScore::SetPlayerName(const cString & strPlayerName)
{
	m_PlayerName = strPlayerName;
}

//  *******************************************************************************************************************
bool cScore::operator> (cScore const &s) const 
{ 
	return m_Score > s.m_Score; 
}

//  *******************************************************************************************************************
bool cScore::operator< (cScore const &s) const 
{ 
	return m_Score < s.m_Score; 
}